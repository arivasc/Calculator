class Calculator{
    int add(int a, int b){ 
	    int c=a+b;	
	    return c; 
    } 
    
    int sub(int a, int b){ 
    	return a-b; 
    }
    
    int mul(int a, int b){
        int mul = a * b;
        return mul;
    }
    
    int div(int a, int b){ 
	int division = a/b;
	return division; 
    }
    
    int mod(int a, int b){
        int mod = a % b;
        return mod; 
    }

}
